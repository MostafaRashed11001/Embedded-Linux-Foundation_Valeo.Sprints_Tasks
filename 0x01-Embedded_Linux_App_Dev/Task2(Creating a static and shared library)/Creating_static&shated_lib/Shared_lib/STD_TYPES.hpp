#ifndef STD_TYPES_HPP
#define STD_TYPES_HPP

// Define standard data types using typedef for improved code readability and portability.

typedef unsigned char      uint8   ; // Unsigned 8-bit integer (0 to 255)
typedef signed char        sint8   ; // Signed 8-bit integer (-128 to 127)
typedef unsigned short int uint32  ; // Unsigned 16-bit integer (0 to 65535)
typedef signed short int   sint32  ; // Signed 16-bit integer (-32768 to 32767)
typedef unsigned long int  uint64  ; // Unsigned 32-bit integer (0 to 4294967295)
typedef signed long int    sint64  ; // Signed 32-bit integer (-2147483648 to 2147483647)
typedef float              float32 ; // Single-precision floating point
typedef double             float64 ; // Double-precision floating point

#endif /* STD_TYPES_HPP_ */